public class ForwardAndBackward {
    
    public static void main(String[] args) {
        System.out.println("   \\/"); // \\ wird zu einem \
        System.out.println("  \\\\//"); 
        System.out.println(" \\\\\\///"); 
        System.out.println(" ///\\\\\\"); 
        System.out.println("  //\\\\"); 
        System.out.println("   /\\"); 

    }
}
